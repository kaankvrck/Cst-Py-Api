def CstDefaultUnits(mws):
    units = mws.Units

    units.Geometry("mm")
    units.Frequency("GHz")
    units.TemperatureUnit("Kelvin")
    units.Time("ns")
    units.Voltage("V")
    units.Current("A")
    units.Resistance("Ohm")
    units.Conductance("Siemens")
    units.Capacitance("PikoF")
    units.Inductance("NanoH")
